// ---------------------------------------------------------------------------
static int Interpolate(	const int* table,
												unsigned long shift,
												unsigned long l2)
{	
	unsigned long mask = (0xFFFFFFF << shift);

	if(l2 == int(l2 & ~mask))
		return table[l2>>shift ];
	else
	{
		mask = ~mask;
		
		const unsigned long smallRes	= table[l2>>shift			];
		const unsigned long	bigRes		= table[(l2>>shift)+1	];

		const unsigned long diff = bigRes - smallRes;

		const unsigned long value = l2 & mask;
				
		const unsigned long res = smallRes + ((value * diff) >> shift);

		return res;
	}
}



