int Lib3D::FSqrtI(int l2)
{
	A_ASSERT(l2>=0);
	int res;
	if (l2 & 0xffff0000)
	{
		if (l2 & 0xff000000)
		{			
			res = ::Interpolate(SqrtTab,20,l2);
		}
		else                 
		{			
			res = ::Interpolate(SqrtTab,12,l2) >> 4;
		}
	}
	else
	{
		if (l2 & 0x0000f000) 
		{
			res = ::Interpolate(SqrtTab,4,l2) >> 8;			
		}
		else
		{
			res =	SqrtTab[l2] >> 10;
		}
	}

	return res;	
}


