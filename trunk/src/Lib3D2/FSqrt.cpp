// ---------------------------------------------------------------------------
int Lib3D::FSqrt(int l2)
{
	A_ASSERT(l2>=0);
	if(l2<0)
		return 1;

	int rc;

	if (l2 & 0xffff0000)
	{
		if (l2 & 0xff000000) 
			rc = SqrtTab[l2>>20];
		else                 
			rc = SqrtTab[l2>>12] >> 4;
	}
	else
	{
		if (l2 & 0x0000f000) 
			rc = SqrtTab[l2>>4] >> 8;
		else                 
			rc = SqrtTab[l2] >> 10;
	}

#if WIN_DEBUG
	double _trueVal = ::sqrt((double)l2);
	double _error = ::fabs(_trueVal-double(rc));
#endif

	return rc;
}


// ---------------------------------------------------------------------------
